# ── C.1 Cleansing ────────────────────────────────────────────────────────
def cleansing(text):
    text = str(text).lower()
    # Hapus URL
    text = re.sub(r'(https?://\S+|www\.\S+|\S+\.co\S+)', ' ', text)
    # Hapus mention & hashtag
    text = re.sub(r'[@#]\w+', ' ', text)
    # Hapus kode hex Excel
    text = re.sub(r'_x[0-9a-zA-Z]{4}_', ' ', text)
    # Buang karakter non-ASCII
    text = text.encode('ascii', 'ignore').decode('ascii')
    # Hapus nomor HP
    text = re.sub(r'(?:\+62|08)[\d\-]{8,15}\b', ' ', text)
    # Hapus resi campuran (min 7 karakter huruf+angka)
    text = re.sub(r'\b(?=[a-zA-Z]*\d)(?=[0-9]*[a-zA-Z])[a-zA-Z0-9]{7,}\b', ' ', text)
    # Hapus angka panjang (resi full angka)
    text = re.sub(r'\b\d{8,}\b', ' ', text)
    # Hapus tanda baca & underscore
    text = re.sub(r'[^\w\s]', ' ', text)
    text = re.sub(r'_', ' ', text)
    # Konversi digit tunggal ke kata
    angka_map = {'0':'nol','1':'satu','2':'dua','3':'tiga','4':'empat',
                 '5':'lima','6':'enam','7':'tujuh','8':'delapan','9':'sembilan'}
    for k, v in angka_map.items():
        text = re.sub(rf'\b{k}\b', v, text)
    # Hapus sisa angka
    text = re.sub(r'\b\d+\b', ' ', text)
    # Bersihkan spasi
    return re.sub(r'\s+', ' ', text).strip()

df['cleans'] = df['text'].apply(cleansing)
