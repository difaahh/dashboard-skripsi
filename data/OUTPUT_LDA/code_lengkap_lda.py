"""---  G.8 Topic Diversity

Topic Diversity digunakan untuk mengukur tingkat keberagaman kata-kata yang digunakan dalam membentuk seluruh topik hasil Topic Modeling.

Perhitungan dilakukan dengan membandingkan jumlah kata unik yang muncul pada seluruh topik terhadap jumlah keseluruhan kata yang diamati.

Semakin tinggi nilai Topic Diversity (mendekati 1), maka semakin beragam kata-kata penyusun topik sehingga menunjukkan kualitas topik yang lebih baik dan tidak banyak mengalami tumpang tindih antar topik.

-- Rumus


```

\[
Topic\ Diversity=\frac{\text{Jumlah Kata Unik}}{\text{Jumlah Top Words}}
\]

```



-- Istilah Penting

- **Unique Words** : Jumlah kata berbeda yang muncul pada seluruh topik.
- **Top Words** : Kata-kata dengan probabilitas terbesar pada setiap topik.
- **Topic Diversity** : Tingkat keberagaman kata antar topik.
"""

# ── G.8 Topic Diversity ────────────────────────────────────────────────

diversity_rows = []

for model_name, lda in LDA_MODELS.items():
    top_words = []

    for topic in range(lda.num_topics):
        words = lda.show_topic(topic, topn=10)
        top_words.extend(
            [w for w,_ in words]
        )

    unique_words = len(set(top_words))
    total_words = len(top_words)
    diversity = unique_words / total_words
    diversity_rows.append({
        "Model":model_name,
        "Jumlah Topik":lda.num_topics,
        "Top Words":total_words,
        "Unique Words":unique_words,
        "Topic Diversity":round(diversity,4)
    })

